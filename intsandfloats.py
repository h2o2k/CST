# Arithmetic operators
# Addition: 3 + 2
# Subtraction: 3 - 2
# Multiplication: 3 * 2
# Division: 3 / 2
# Exponent: 3 ** 2
# Modulus: 3 // 2

# Comparison operators
# Equal: 3 == 2
# Not equal: 3 != 2
# Greater than: 3 > 2
# Less than: 3 < 2
# Greater or equal: 3 >= 2
# Less than or equal: 3 =< 2

# Returning a data type
num1 = 3
num2 = 3.14
print(type(num1))
print(type(num2))

# Basic operators
print(3 + 2)
print(3 - 2)
print(3 * 2)
print(3 / 2)

# Dropping the decimal
print(3 // 2)

# Powers
print(3 ** 2)

# Remainder after a division
print(3 % 2)

# Order of operations (BIDMAS)
print(3 * (2 + 1))

# Incrementing a variable
num3 = 1
num3 += 5
print(num3)

# Absolute values
print(abs(-3))

# Rounding
print(round(3.75))
